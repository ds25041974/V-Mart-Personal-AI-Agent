def convert_md_to_html(md_file: Path) -> bool:
    """Convert markdown to HTML for PDF printing"""
    try:
        # Read markdown
        with open(md_file, "r", encoding="utf-8") as f:
            md_content = f.read()

        # Convert to HTML
        html_content = markdown2.markdown(
            md_content,
            extras=[
                "fenced-code-blocks",
                "tables",
                "header-ids",
                "toc",
                "code-friendly",
                "strike",
                "task_list",
            ],
        )

        # Create title
        title = md_file.stem.replace("_", " ").replace("-", " ").title()

        # Create full HTML
        full_html = f"""
<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>{title} - V-Mart AI Agent</title>
    {PDF_CSS}
    <script>
        function printToPDF() {{
            window.print();
        }}
    </script>
</head>
<body>
    <button class="print-button" onclick="printToPDF()">ğŸ–¨ï¸ Print to PDF</button>
    {create_html_header(title)}
    {html_content}
    <hr>
    <p style="text-align: center; color: #999; font-size: 0.9em;">
        Generated by V-Mart Personal AI Agent Documentation System<br>
        Â© 2025 All Rights Reserved
    </p>
</body>
</html>
"""

        # Save HTML
        html_file = HTML_OUTPUT_DIR / f"{md_file.stem}.html"
        with open(html_file, "w", encoding="utf-8") as f:
            f.write(full_html)

        size_kb = html_file.stat().st_size / 1024
        print(f"âœ“ {md_file.name:45s} â†’ {html_file.name:45s} ({size_kb:.1f} KB)")
        return True

    except Exception as e:
        print(f"âœ— {md_file.name:45s} â†’ Error: {str(e)}")
        return False


